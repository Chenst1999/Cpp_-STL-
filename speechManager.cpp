void SpeechManager::createSpeaker() {
	string nameSeed = "ABCDEFGHIJKL";
	for (int i = 0; i < nameSeed.size(); i++)
	{
		string name = "选手";
		name += nameSeed[i];
		Speaker sp;
		sp.m_name = name;
		for (int j = 0; j < 2; j++) {
			sp.m_score[j] = 0;
		}
		this->v1.push_back(i + 10001);
		this->m_speaker.insert(make_pair(i+10001,sp));
	}
}

