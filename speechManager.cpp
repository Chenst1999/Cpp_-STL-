void SpeechManager::speechContest() {
	cout << "---------------第" << this->m_index << "轮比赛正式开始---------------" << endl;
	multimap<double, int, greater<double>>groupScore;
	int num = 0;
	vector<int>v_src;
	if (this->m_index == 1) {
		v_src = v1;
	}
	else {
		v_src = v2;
	}
	for (vector<int>::iterator it = v_src.begin(); it != v_src.end(); it++) {
		num++;
		deque<double> d;
		for (int i = 0; i < 10; i++) {
			double score = (rand() % 401 + 600)/10.f;
			//cout << score << "  ";
			d.push_back(score);
		}
		//cout << endl;
		sort(d.begin(), d.end(), greater<double>());
		d.pop_front();
		d.pop_back();
		double sum = accumulate(d.begin(), d.end(), 0.0f);
		double avg = sum / (double)d.size();
		//cout <<"num:"<<*it<<"	name:"<<this->m_speaker[*it].m_name<<"  avg:" << avg << endl;
		this->m_speaker[*it].m_score[this->m_index - 1] = avg;
		groupScore.insert(make_pair(avg, *it));//key是得分，value是选手编号
		if (num % 6 == 0) {
			cout << "第" << num / 6 << "小组比赛名次:" << endl;
			for (multimap<double, int, greater<double>>::iterator it = groupScore.begin(); it != groupScore.end(); it++) {
				cout << "编号：" << it->second << "		姓名：" << this->m_speaker[it->second].m_name 
					<< "		成绩：" << this->m_speaker[it->second].m_score[this->m_index-1] << endl;
			}
			//取走前三名
			int count = 0;
			for (multimap<double, int, greater<double>>::iterator it = groupScore.begin(); it != groupScore.end()&&count<3; count++,it++) {
				if (this->m_index == 1) {
					v2.push_back((*it).second);
				}
				else {
					vVictory.push_back(it->second);
				}
			}
			groupScore.clear();
			cout << endl;
		}
	}
	cout << "---------------第" << this->m_index << "轮比赛完毕---------------" << endl;
	system("pause");
}

